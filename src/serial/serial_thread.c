void* serial_thread_start(void* arg)
{
    /* arg need to have id of thread */
    while(1)
    {
        /* check signal */
        if(serial_signal_check_signal(5))
        {
            LOG_INFO("Signal is waiting in queue\n");
            SignalS* signal = serial_signal_get(5);
            serial_signal_handler(signal);
        }
    }
}
